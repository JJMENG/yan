package com.tstar.callcenter.model;

/**
 * json实体 无库表
 * @author lixm
 *
 */
public class JsonInfo {
	
	private long id;
	
	private String[] children;

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String[] getChildren() {
		return children;
	}

	public void setChildren(String[] children) {
		this.children = children;
	}
	
	

}
